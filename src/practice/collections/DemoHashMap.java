package practice.collections;

import java.util.HashMap;

public class DemoHashMap {

	public static void main(String[] args) {
		HashMap map = new HashMap<>();
		map.put(3, null);
		map.put(5, "jia");
		map.put("priya", "kiya");
		map.put(2, "riya");
		map.put(10.25, "kiya");
		map.put(7, null);
		System.out.println(map);
	}

}
